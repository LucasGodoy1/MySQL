--Passo 1 Crie um esquema (schema)
create schema JV;

--O comando "CREATE SCHEMA" no SQL é usado para criar um novo esquema (schema)
--em um banco de dados. Um esquema é uma estrutura que pode conter tabelas,
--visualizações, funções, procedimentos armazenados e outros objetos relacionados.
--Ele é usado principalmente para organizar e agrupar
--objetos de banco de dados relacionados.